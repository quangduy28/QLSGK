  private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton3ActionPerformed
    Pattern pattern = Pattern.compile("^(0?[1-9]|[12][0-9]|3[01])-(0?[1-9]|1[012])-([12][0-9]{3})$");
    Matcher matcher = pattern.matcher(txtsearch.getText());
    if (matcher.matches()) {
      int n = 0;
      ArrayList<phieuXuat> list2 = new phieuXuatDAO().getList();
      ArrayList<phieuXuat> list3 = new ArrayList<>();
      for (phieuXuat pn : list2) {
        if (pn.getNgay().contains(txtsearch.getText().trim())) {
          list3.add(pn);
          n++;
        }
      }
      if (n == 0) {
        JOptionPane.showMessageDialog(this, "Không tìm thấy!");
      }
      list.clear();
      list.addAll(list3);
      i = 1;
      loadArray();
    } else {
      JOptionPane.showMessageDialog(this, "Không đúng định dạng ngày!");
    }

  }// GEN-LAST:event_jButton3ActionPerformed

