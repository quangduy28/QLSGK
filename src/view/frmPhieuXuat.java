  private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton2ActionPerformed
    if (txtmapx.getText().equals("")) {
      JOptionPane.showMessageDialog(this, "Nhập mã phiếu cần xóa!");
      txtmapx.requestFocus();
    }
    phieuXuat pn = new phieuXuat();
    pn.setMaPX(txtmapx.getText());

    if (new phieuXuatDAO().delete2(pn)) {
      JOptionPane.showMessageDialog(this, "Xóa thành công! ");
      list = new phieuXuatDAO().getList();
      i = 1;
      loadArray();
    }
  }// GEN-LAST:event_jButton2ActionPerformed

  private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_jButton1ActionPerformed

    if (txtmapx.getText().equals("") || txtmanv.getText().equals("") || txtmasach.getText().equals("")
        || txtsl.getText().equals("") || txtncc.getText().equals("")) {
      JOptionPane.showMessageDialog(this, "Vui lòng nhập đủ thông tin!");
      return;
    }
    phieuXuat pn = new phieuXuat();
    pn.setMaPX(txtmapx.getText());
    pn.setMaNV(txtmanv.getText());
    pn.setMaNCC(txtncc.getText());
    pn.setMaSGK(txtmasach.getText());
    pn.setNgay(txtngay.getText());
    pn.setSoLuong(Integer.valueOf(txtsl.getText()));

    if (!(txtncc.getText().equals(""))) {
      String mancc = txtncc.getText();
      int n = 0;
      ArrayList<nhaCC> list = new nhaCCDAO().getList();
      for (nhaCC ncc : list) {
        if (mancc.equals(ncc.getMaNhaCC())) {
          n++;
        }
      }
      if (n != 1) {
        JOptionPane.showMessageDialog(this, "Không có mã nhà cung cấp này!");
        return;
      }
    }

    if (!(txtmasach.getText().equals(""))) {
      String mancc = txtmasach.getText();
      int n = 0;
      ArrayList<sachGiaoKhoa> list = new sachGiaoKhoaDAO().getList();
      for (sachGiaoKhoa ncc : list) {
        if (mancc.equals(ncc.getMaSach())) {
          n++;
        }
      }
      if (n != 1) {
        JOptionPane.showMessageDialog(this, "Không có mã sách này!");
        return;
      }
    }

    list.add(pn);
    if (new phieuXuatDAO().insert2(pn)) {
      JOptionPane.showMessageDialog(this, "Add success!");
      i = 1;
      loadArray();

      txtmapx.setText("");
      txtncc.setText("");
      txtmasach.setText("");
      txtsl.setText("");
    } else {
      JOptionPane.showMessageDialog(this, "Mã phiếu không được trùng nhau!");

    }

  }// GEN-LAST:event_jButton1ActionPerformed

